package com.energyman.bean;

public class BuildingType {
	private String buildType_Name;
	
	private Integer buildType_ID;
	
	private String buildType_Desc;

	public String getBuildType_Name() {
		return buildType_Name;
	}

	public void setBuildType_Name(String buildType_Name) {
		this.buildType_Name = buildType_Name;
	}

	public Integer getBuildType_ID() {
		return buildType_ID;
	}

	public void setBuildType_ID(Integer buildType_ID) {
		this.buildType_ID = buildType_ID;
	}

	public String getBuildType_Desc() {
		return buildType_Desc;
	}

	public void setBuildType_Desc(String buildType_Desc) {
		this.buildType_Desc = buildType_Desc;
	}

	
	
}
