package com.energyman.bean;

public class Area {
	private Integer region_ID;
	
	private String region_Name;
	
	private Integer park_ID;
	
	private String region_Addr;
	
	private String region_Contact;
	
	private String region_Tel;
	
	private String region_Email;

	public Integer getRegion_ID() {
		return region_ID;
	}

	public void setRegion_ID(Integer region_ID) {
		this.region_ID = region_ID;
	}

	public String getRegion_Name() {
		return region_Name;
	}

	public void setRegion_Name(String region_Name) {
		this.region_Name = region_Name;
	}

	public Integer getPark_ID() {
		return park_ID;
	}

	public void setPark_ID(Integer park_ID) {
		this.park_ID = park_ID;
	}

	public String getRegion_Addr() {
		return region_Addr;
	}

	public void setRegion_Addr(String region_Addr) {
		this.region_Addr = region_Addr;
	}

	public String getRegion_Contact() {
		return region_Contact;
	}

	public void setRegion_Contact(String region_Contact) {
		this.region_Contact = region_Contact;
	}

	public String getRegion_Tel() {
		return region_Tel;
	}

	public void setRegion_Tel(String region_Tel) {
		this.region_Tel = region_Tel;
	}

	public String getRegion_Email() {
		return region_Email;
	}

	public void setRegion_Email(String region_Email) {
		this.region_Email = region_Email;
	}

	
	
}	
