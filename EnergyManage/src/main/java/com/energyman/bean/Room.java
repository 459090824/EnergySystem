package com.energyman.bean;

public class Room {
	private String room_Name;
	
	private Integer room_ID;
	
	private Integer room_Code;
	
	private Integer build_ID;
	
	private Integer room_Floor;
	
	private Double room_Area;
	
	private Double roomEasementOfLight;

	public String getRoom_Name() {
		return room_Name;
	}

	public void setRoom_Name(String room_Name) {
		this.room_Name = room_Name;
	}

	public Integer getRoom_ID() {
		return room_ID;
	}

	public void setRoom_ID(Integer room_ID) {
		this.room_ID = room_ID;
	}

	public Integer getRoom_Code() {
		return room_Code;
	}

	public void setRoom_Code(Integer room_Code) {
		this.room_Code = room_Code;
	}

	public Integer getBuild_ID() {
		return build_ID;
	}

	public void setBuild_ID(Integer build_ID) {
		this.build_ID = build_ID;
	}

	public Integer getRoom_Floor() {
		return room_Floor;
	}

	public void setRoom_Floor(Integer room_Floor) {
		this.room_Floor = room_Floor;
	}

	public Double getRoom_Area() {
		return room_Area;
	}

	public void setRoom_Area(Double room_Area) {
		this.room_Area = room_Area;
	}

	public Double getRoomEasementOfLight() {
		return roomEasementOfLight;
	}

	public void setRoomEasementOfLight(Double roomEasementOfLight) {
		this.roomEasementOfLight = roomEasementOfLight;
	}

	
}
