package com.energyman.bean;

public class MeterType {
	private String meterType_Name;
	
	private Integer meterType_ID;
	
	private String meterType_Desc;

	public String getMeterType_Name() {
		return meterType_Name;
	}

	public void setMeterType_Name(String meterType_Name) {
		this.meterType_Name = meterType_Name;
	}

	public Integer getMeterType_ID() {
		return meterType_ID;
	}

	public void setMeterType_ID(Integer meterType_ID) {
		this.meterType_ID = meterType_ID;
	}

	public String getMeterType_Desc() {
		return meterType_Desc;
	}

	public void setMeterType_Desc(String meterType_Desc) {
		this.meterType_Desc = meterType_Desc;
	}

	
	
}
