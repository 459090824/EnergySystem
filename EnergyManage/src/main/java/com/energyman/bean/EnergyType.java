package com.energyman.bean;


public class EnergyType {
	private String energyType_Name;
	
	private Integer energyType_ID;
	
	private String energyType_Desc;

	public String getEnergyType_Name() {
		return energyType_Name;
	}

	public void setEnergyType_Name(String energyType_Name) {
		this.energyType_Name = energyType_Name;
	}

	public Integer getEnergyType_ID() {
		return energyType_ID;
	}

	public void setEnergyType_ID(Integer energyType_ID) {
		this.energyType_ID = energyType_ID;
	}

	public String getEnergyType_Desc() {
		return energyType_Desc;
	}

	public void setEnergyType_Desc(String energyType_Desc) {
		this.energyType_Desc = energyType_Desc;
	}

	
	
}
