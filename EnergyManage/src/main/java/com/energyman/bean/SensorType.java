package com.energyman.bean;

public class SensorType {
	private String sensorType_Name;
	
	private Integer sensorType_ID;
	
	private String sensorType_Desc;

	public String getSensorType_Name() {
		return sensorType_Name;
	}

	public void setSensorType_Name(String sensorType_Name) {
		this.sensorType_Name = sensorType_Name;
	}

	public Integer getSensorType_ID() {
		return sensorType_ID;
	}

	public void setSensorType_ID(Integer sensorType_ID) {
		this.sensorType_ID = sensorType_ID;
	}

	public String getSensorType_Desc() {
		return sensorType_Desc;
	}

	public void setSensorType_Desc(String sensorType_Desc) {
		this.sensorType_Desc = sensorType_Desc;
	}

	
	
}
