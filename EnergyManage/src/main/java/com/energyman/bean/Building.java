package com.energyman.bean;

public class Building {
	private String build_Name;
	
	private Integer build_ID;
	
	private Integer region_ID;
	
	private Integer buildType_ID;
	
	private Integer build_State;
	
	private Integer buildUp_Floor;
	
	private Integer buildDown_Floor;

	public String getBuild_Name() {
		return build_Name;
	}

	public void setBuild_Name(String build_Name) {
		this.build_Name = build_Name;
	}

	public Integer getBuild_ID() {
		return build_ID;
	}

	public void setBuild_ID(Integer build_ID) {
		this.build_ID = build_ID;
	}

	public Integer getRegion_ID() {
		return region_ID;
	}

	public void setRegion_ID(Integer region_ID) {
		this.region_ID = region_ID;
	}

	public Integer getBuildType_ID() {
		return buildType_ID;
	}

	public void setBuildType_ID(Integer buildType_ID) {
		this.buildType_ID = buildType_ID;
	}

	public Integer getBuild_State() {
		return build_State;
	}

	public void setBuild_State(Integer build_State) {
		this.build_State = build_State;
	}

	public Integer getBuildUp_Floor() {
		return buildUp_Floor;
	}

	public void setBuildUp_Floor(Integer buildUp_Floor) {
		this.buildUp_Floor = buildUp_Floor;
	}

	public Integer getBuildDown_Floor() {
		return buildDown_Floor;
	}

	public void setBuildDown_Floor(Integer buildDown_Floor) {
		this.buildDown_Floor = buildDown_Floor;
	}

	
	
	
}
